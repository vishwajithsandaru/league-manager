package responses;

import java.time.LocalDate;

public class MatchResponse implements Comparable<MatchResponse> {

    private String team1;
    private String team2;
    private String score1;
    private String score2;
    private String date;
    private String time;
    private String status;

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getTeam1() {
        return team1;
    }

    public void setTeam1(String team1) {
        this.team1 = team1;
    }

    public String getTeam2() {
        return team2;
    }

    public void setTeam2(String team2) {
        this.team2 = team2;
    }

    public String getScore1() {
        return score1;
    }

    public void setScore1(String score1) {
        this.score1 = score1;
    }

    public String getScore2() {
        return score2;
    }

    public void setScore2(String score2) {
        this.score2 = score2;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }


    @Override
    public int compareTo(MatchResponse matchResponse) {

        LocalDate localDate1 = LocalDate.parse(date);
        LocalDate localDate2 = LocalDate.parse(matchResponse.getDate());

        return localDate1.compareTo(localDate2);

    }
}
